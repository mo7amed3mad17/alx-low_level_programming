#include <stdio.h>
/**
  * add - add numbers
  * @n1: num1
  * @n2: num2
  * Return: int
 */
int add(int n1, int n2)
{
	return (n1 + n2);
}
/**
  * sub - sub numbers
  * @n1: num1
  * @n2: num2
  * Return: int
 */
int sub(int n1, int n2)
{
	return (n1 - n2);
}
/**
  * mul - mul numbers
  * @n1: num1
  * @n2: num2
  * Return: int
 */
int mul(int n1, int n2)
{
	return (n1 * n2);
}
/**
  * div - div numbers
  * @n1: num1
  * @n2: num2
  * Return: int
 */
int div(int n1, int n2)
{
	return (n1 / n2);
}
/**
  * mod - mod numbers
  * @n1: num1
  * @n2: num2
  * Return: int
 */
int mod(int n1, int n2)
{
	return (n1 % n2);
}
